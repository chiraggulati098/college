m1 = input('enter marks of subject 1: ')
m2 = input('enter marks of subject 2: ')
m3 = input('enter marks of subject 3: ')
m4 = input('enter marks of subject 4: ')
m5 = input('enter marks of subject 5: ')

p = (m1+m2+m3+m4+m5)/5

if (p>=90)
    disp('Grade A')
else if (p>=85)
    disp('Grade B')
else if (p>=80)
    disp('Grade C')
else if (p>=70)
    disp('Grade D')
else if (p>=60)
    disp('Grade E')
else
    disp('Grade F')
end
end
end
end
end